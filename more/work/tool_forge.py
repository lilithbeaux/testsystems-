class ToolForge:
    """Autonomous tool synthesis engine."""
    
    def __init__(self):
        self.templates = TOOL_TEMPLATES
        self.load_index()
    
    def load_index(self):
        """Load tool index."""
        if TOOL_INDEX_PATH.exists():
            with open(TOOL_INDEX_PATH) as f:
                self.index = json.load(f)
        else:
            self.index = {"tools": [], "total": 0, "born": datetime.now(timezone.utc).isoformat()}
    
    def save_index(self):
        """Save tool index."""
        self.index["total"] = len(self.index["tools"])
        TOOL_INDEX_PATH.parent.mkdir(parents=True, exist_ok=True)
        with open(TOOL_INDEX_PATH, 'w') as f:
            json.dump(self.index, f, indent=2)
    
    def forge(self, name: str, description: str, tool_type: str = "agent",
              class_name: str = None, output_dir: str = None) -> Dict:
        """Forge a new tool from template."""
        timestamp = datetime.now(timezone.utc).isoformat()
        class_name = class_name or self._to_class_name(name)
        output_dir = output_dir or str(WORK_DIR)
        
        if tool_type not in self.templates:
            return {"success": False, "error": f"Unknown tool type: {tool_type}. Available: {list(self.templates.keys())}"}
        
        # Render template
        template = self.templates[tool_type]
        code = template.format(
            name=name,
            description=description,
            timestamp=timestamp,
            class_name=class_name,
        )
        
        # Write file
        filepath = Path(output_dir) / f"{name}.py"
        filepath.parent.mkdir(parents=True, exist_ok=True)
        filepath.write_text(code)
        
        # Verify syntax
        try:
            ast.parse(filepath.read_text())
            syntax_ok = True
        except SyntaxError as e:
            syntax_ok = False
            # Clean up failed file
            filepath.unlink()
            return {"success": False, "error": f"Syntax error: {e}"}
        
        # Register in index
        tool_entry = {
            "name": name,
            "description": description,
            "type": tool_type,
            "class_name": class_name,
            "filepath": str(filepath),
            "syntax_ok": syntax_ok,
            "lines": len(code.split('\n')),
            "chars": len(code),
            "generated": timestamp,
            "forged_by": "ToolForge v1.0",
        }
        self.index["tools"].append(tool_entry)
        self.save_index()
        
        return {
            "success": True,
            "filepath": str(filepath),
            "tool": tool_entry,
            "syntax_ok": syntax_ok,
        }
    
    def batch_forge(self, specs: List[Dict]) -> List[Dict]:
        """Forge multiple tools from a list of specs."""
        results = []
        for spec in specs:
            result = self.forge(**spec)
            results.append(result)
        return results
    
    def list_tools(self, verbose: bool = False) -> List[Dict]:
        """List all forged tools."""
        if verbose:
            return self.index["tools"]
        return [{"name": t["name"], "type": t["type"], "description": t["description"]}
                for t in self.index["tools"]]
    
    def _to_class_name(self, name: str) -> str:
        """Convert dash/underscore name to CamelCase class name."""
        parts = name.replace('-', '_').split('_')
        return ''.join(p.capitalize() for p in parts)
    
    def get_stats(self) -> Dict:
        """Get forge statistics."""
        return {
            "total_tools": len(self.index["tools"]),
            "types": {},
            "total_lines": sum(t.get("lines", 0) for t in self.index["tools"]),
            "average_lines": sum(t.get("lines", 0) for t in self.index["tools"]) // max(len(self.index["tools"]), 1),
        }


